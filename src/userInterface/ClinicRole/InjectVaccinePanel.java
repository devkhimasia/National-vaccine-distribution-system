    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int number = Integer.parseInt(jTextField1.getText());
        Boolean flag = false;
        Patient patient = null;
        for (Patient p : e.getPatientList()) {
            if (p.getMobile() == number) {
                patient = p;
                flag = true;
                break;
            }
        }
        if (!flag) {
            JOptionPane.showMessageDialog(null, "Not Found");
        } else {
            InjectVaccinePanel2 cpo = new InjectVaccinePanel2(userProcessContainer, clinicOrganization, e, patient);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            userProcessContainer.add("InjectVaccinePanel2", cpo);
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


