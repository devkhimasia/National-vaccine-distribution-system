    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
try{
        
                                        HospitalOrganization o = (HospitalOrganization) cmbHosp.getSelectedItem();
                                        ClinicOrganization e = new ClinicOrganization(txtName.getText());
                                        o.getOrganizationDirectory().getOrganizationList().add(e);

                                        e.setStreet(txtStreet.getText());
                                        e.setPin(Integer.parseInt(txtPin.getText()));
                                        e.setContact(Integer.parseInt(txtContact.getText()));
                                        e.setEmail(txtEmail.getText());
                                        e.setStatus("Inactive");
                                        e.setState(cmbState.getSelectedItem().toString());
                                        e.setCity(txtCity.getText());
                                        AddNewHospitalRequest a = new AddNewHospitalRequest();
                                        a.setOrganization(e);
                                        a.setStatus("Pending");
                                        o.getWorkQueue().getWorkRequestList().add(a);
                                    
        }                                        
    catch(ConcurrentModificationException e){
        
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void cmbCountryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbCountryActionPerformed
        // TODO add your handling code here:
        cmbState.removeAllItems();
        Network country = (Network) cmbCountry.getSelectedItem();
        if (country != null) {
            for (Network n : country.getStateList()) {
                cmbState.addItem(n);
            }
        }
    }//GEN-LAST:event_cmbCountryActionPerformed

    private void cmbStateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbStateActionPerformed
        cmbHosp.removeAllItems();
        Network n = (Network) cmbState.getSelectedItem();
        if (n != null) {
            Enterprise e = null;
            for (Enterprise e1 : n.getEnterpriseDirectory().getEnterpriseList()) {
                if (e1 instanceof PhdEnterprise) {
                    for (Organization o : e1.getOrganizationDirectory().getOrganizationList()) {
                        if (o instanceof HospitalOrganization) {
                            HospitalOrganization htemp = (HospitalOrganization) o;
                            if(htemp.getStatus()=="Active")
                            cmbHosp.addItem(o);
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_cmbStateActionPerformed

