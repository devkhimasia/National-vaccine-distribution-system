    private void btnDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailsActionPerformed
        int selected = tblReq.getSelectedRow();
        if(selected <0){
            return;
        }
        else{
            WorkRequest w= (WorkRequest) tblReq.getValueAt(selected, 0);
            ClinicDetailsJPanel h= new ClinicDetailsJPanel(userProcessContainer,w,o);
            CardLayout l = (CardLayout) userProcessContainer.getLayout();
            userProcessContainer.add("new_hosp_details",h);
            l.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnDetailsActionPerformed


