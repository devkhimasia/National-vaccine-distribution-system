public void populate(){
    DefaultTableModel dtm = (DefaultTableModel) tblReq.getModel();
    dtm.setRowCount(0);
    for(WorkRequest w: o.getWorkQueue().getWorkRequestList()){
        if (w instanceof AddNewHospitalRequest){
            AddNewHospitalRequest a= (AddNewHospitalRequest)w;
            Object row[] = new Object[3];
            row[0] = a;
            row[1] = a.getOrganization();
            row[2] = a.getRequestDate();
            
            dtm.addRow(row);
        }
    }
}
