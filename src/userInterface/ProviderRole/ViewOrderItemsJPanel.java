public void populate(){
    DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
    dtm.setRowCount(0);
    Order o = cor.getOrder();
    for(OrderItem oi: o.getOrder()){
        Object row[] = new Object[2];
        row[0] = oi.getProduct().getName();
        row[1] = oi.getQuantity();
        dtm.addRow(row);
    }
}
